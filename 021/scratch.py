#notes about slice on Python

piano = [ "a", "b", "c", "d", "e", "f", "g"]
print("print everything")
print(piano)

print("print start from second and up 5th element")
print(piano[1:5])

print("print the only 3 first")
print(piano[:3])

print("print every odd element")
print(piano[0:7:2])

print("print in reverse order")
print(piano[::-1])